    private void btn_DelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DelActionPerformed
        if (_iSinhVienService.getlstStudents().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Danh sách trống");
            return;
        }
        int index = tbl_SinhVien.getSelectedRow();//Lấy được vị trí người dùng bấm
        //Chưa làm hành động bấm vào load lên trên
        JOptionPane.showMessageDialog(this, _iSinhVienService.delete(String.valueOf(_iSinhVienService.getlstStudents().get(index).getId())));
        loadData(_iSinhVienService.getlstStudents());
    }//GEN-LAST:event_btn_DelActionPerformed

    private void txt_FullNameMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txt_FullNameMouseExited
        if (txt_FullName.getText().isBlank()) {
            txt_MaSV.setText("");
            return;
        }
        else if (_Utilities.checkSo(txt_FullName.getText())) {
            txt_FullName.setForeground(Color.red);
            lbl_FBIwarning.setVisible(true);
            btn_Add.setEnabled(false);
            return;
        }
        txt_FullName.setForeground(Color.black);
        lbl_FBIwarning.setVisible(false);
        btn_Add.setEnabled(true);
        String name = _Utilities.convertFullName(txt_FullName.getText().toLowerCase());
        txt_FullName.setText(name);
        txt_MaSV.setText(_Utilities.msvFpoly(name, _iSinhVienService.getMaxID(), 0));
    }//GEN-LAST:event_txt_FullNameMouseExited

