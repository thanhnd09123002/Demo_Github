    void loadData(List<Student> data) {
        _dDefaultTableModel = (DefaultTableModel) tbl_SinhVien.getModel();
        _dDefaultTableModel.setRowCount(0);
        int stt = 1;
        for (Student x : data) {
            _dDefaultTableModel.addRow(new Object[]{stt++, x.getId(), x.getTen(), x.getSdt(), x.getMsv(), x.getNganhHoc() == 1 ? "UDPM.JAVA" : x.getNganhHoc() == 2 ? "UDPM.NET" : "MOB.IOS", x.getGioitinh() == 1 ? "Nam" : x.getGioitinh() == 2 ? "Nữ" : "Không xác định"});
        }
        getMAXID();
    }

    void getMAXID() {
        txt_Id.setText(String.valueOf(_iSinhVienService.getMaxID()));
    }

    Student getDataGui() {//Trả về 1 đối tượng lấy từ Control
        //Nếu không code được đoạn này thì khai báo 1 Student xong rồi chám từng thuộc tính gắn cho control
        return new Student(
                Integer.parseInt(txt_Id.getText()),
                txt_FullName.getText(),
                txt_PhoneNumber.getText(),
                txt_MaSV.getText(),
                (cbc_NganhHoc.getSelectedItem() == "UDPM.JAVA" ? 1 : cbc_NganhHoc.getSelectedItem() == "UDPM.NET" ? 2 : 3),
                (cbc_GioiTinh.getSelectedItem() == "Nam" ? 1 : cbc_GioiTinh.getSelectedItem() == "Nữ" ? 2 : 3)
        );
    }

